#pragma region "main function"
int main()
{
	// init glfw
	glfwInit();
	glfwWindowHint(GLFW_CONTEXT_VERSION_MAJOR, 3);
	glfwWindowHint(GLFW_CONTEXT_VERSION_MINOR, 3);
	glfwWindowHint(GLFW_OPENGL_PROFILE, GLFW_OPENGL_CORE_PROFILE);
	glfwWindowHint(GLFW_RESIZABLE, GL_FALSE);

	// create windows
	GLFWwindow* window = glfwCreateWindow(800, 600, "LearnOpenGL", nullptr, nullptr);
	if (nullptr == window)
	{
		std::cout << "fail to create glfw window" << std::endl;
		glfwTerminate();
		return -1;
	}

	// set context
	glfwMakeContextCurrent(window);

	// init glew
	glewExperimental = GL_TRUE;
	if (GLEW_OK != glewInit())
	{
		std::cout << "fail to init glew" << std::endl;
		return -1;
	}

	// set viewport	
	glfwGetFramebufferSize(window, &SCR_WIDTH, &SCR_HEIGHT);
	glViewport(0, 0, SCR_WIDTH, SCR_HEIGHT);

	// set keycallback
	glfwSetKeyCallback(window, key_callback);
	glfwSetCursorPosCallback(window, mouse_callback);
	glfwSetInputMode(window, GLFW_CURSOR, GLFW_CURSOR_DISABLED);
	glfwSetScrollCallback(window, scroll_callback);

#pragma region "resource"
	Shader depthmapShader("depthmap.vs", "depthmap.frag");
	Shader quadShader("quad.vs", "quad.frag");
	Shader sceneShader("scene.vs", "scene.frag");
	if (sceneTexID == 0)
	{
		sceneTexID = loadTexture("wood.png", false, true);
	}
#pragma endregion

	// run loop
	while (!glfwWindowShouldClose(window))
	{
		GLfloat currentFrame = glfwGetTime();
		deltaTime = currentFrame - lastFrame;
		lastFrame = currentFrame;

		// check key or mouse events
		glfwPollEvents();
		do_movement();

		glEnable(GL_CULL_FACE);
		glCullFace(GL_FRONT);

		// step1 render depth map
		renderDepthMap(depthmapShader);
		glCullFace(GL_BACK);
		glDisable(GL_CULL_FACE);

		// test render to quad
		//renderQuad(quadShader);

		glEnable(GL_FRAMEBUFFER_SRGB);

		// step2 render scene
		renderScene(sceneShader);		

		// swap buffer
		glfwSwapBuffers(window);
	}

	glfwTerminate();

	return 0;
}
